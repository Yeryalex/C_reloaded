#include <stdio.h>
#include <string.h>

int	ft_strcmp(char *s1, char *s2)
{
	while (*s1 && *s1 == *s2)
	{
		s1++;
		s2++;
	}
	return ((unsigned char)*s1 - (unsigned char)*s2);
}
int main()
{
	printf("%i\n", ft_strcmp("ho", "hol"));
	printf("%i\n", strcmp("ho", "hol"));
	return (0);
}
